package bean;

/**
 * The Class Prerequisites.
 */
/**
 * @author naveen
 *
 */
public class Prerequisites {

/** The dept code. */
private String deptCode;

/** The course number. */
private long courseNumber;

/** The preq dept code. */
private String preqDeptCode;

/** The preq course number. */
private String preqCourseNumber;

/**
 * Gets the dept code.
 *
 * @return the deptCode
 */
public String getDeptCode() {
	return deptCode;
}

/**
 * Sets the dept code.
 *
 * @param deptCode the deptCode to set
 */
public void setDeptCode(String deptCode) {
	this.deptCode = deptCode;
}

/**
 * Gets the course number.
 *
 * @return the courseNumber
 */
public long getCourseNumber() {
	return courseNumber;
}

/**
 * Sets the course number.
 *
 * @param courseNumber the courseNumber to set
 */
public void setCourseNumber(long courseNumber) {
	this.courseNumber = courseNumber;
}

/**
 * Gets the preq dept code.
 *
 * @return the preqDeptCode
 */
public String getPreqDeptCode() {
	return preqDeptCode;
}

/**
 * Sets the preq dept code.
 *
 * @param preqDeptCode the preqDeptCode to set
 */
public void setPreqDeptCode(String preqDeptCode) {
	this.preqDeptCode = preqDeptCode;
}

/**
 * Gets the preq course number.
 *
 * @return the preqCourseNumber
 */
public String getPreqCourseNumber() {
	return preqCourseNumber;
}

/**
 * Sets the preq course number.
 *
 * @param preqCourseNumber the preqCourseNumber to set
 */
public void setPreqCourseNumber(String preqCourseNumber) {
	this.preqCourseNumber = preqCourseNumber;
}

}
